# funzione che serve a specchiare le mappe di calore introdotte, perchÃ© gli indici utilizzati per le case sono invertiti
function flip_table!(table::Vector{Int64})
    @assert length(table) == 64
    for i in 1:32
        table[i], table[65 - i] = table[65 - i], table[i]
    end
end

